public class Real{
    
    //Aquí se van a poner las operaciones básicas para los números reales
    private double numero;

    // Constructor
    public Real(double numero) {
        this.numero = numero;
    }
    
    
    // Getters and Setters
    public double getNumero() {
        return numero;
    }
    
    public void setNumero(double numero) {
        this.numero = numero;
    }
    
}
